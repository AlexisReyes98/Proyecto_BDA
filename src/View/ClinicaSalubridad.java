    public void listar20kDatos() { // Con esta función se muestra la tabla
        pacientes = db.ListPacientes20k();
        DefaultTableModel tb = (DefaultTableModel)jTable1.getModel();
        for(Pacientes p: pacientes) {
            tb.addRow(new Object[]{p.getID_REGISTRO(), p.getSEXO(), p.getENTIDAD_RES(), 
            p.getTIPO_PACIENTE(), p.getFECHA_INGRESO(), p.getFECHA_SINTOMAS(), p.getEDAD(), 
            p.getRESULTADO_LAB(), p.getRESULTADO_ANTIGENO(), p.getCLASIFICACION_FINAL()});
        }
    }
    
