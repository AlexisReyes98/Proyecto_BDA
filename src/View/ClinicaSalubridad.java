    private void BotonBuscar20kActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonBuscar20kActionPerformed
        if (!"".equals(jTextField1.getText())) {
            String id_p = jTextField1.getText();
            paciente = db.buscarPaciente20k(id_p);
            if (paciente.getID_REGISTRO() != null) {
                JOptionPane.showMessageDialog(null, "Paciente Encontrado","",JOptionPane.INFORMATION_MESSAGE);
                jTextField1.setText("");
                limpiarFormulario();
                DefaultTableModel tb = (DefaultTableModel)jTable1.getModel();
                tb.addRow(new Object[]{paciente.getID_REGISTRO(), paciente.getSEXO(), paciente.getENTIDAD_RES(), 
                paciente.getTIPO_PACIENTE(), paciente.getFECHA_INGRESO(), paciente.getFECHA_SINTOMAS(), paciente.getEDAD(), 
                paciente.getRESULTADO_LAB(), paciente.getRESULTADO_ANTIGENO(), paciente.getCLASIFICACION_FINAL()});
            } else {
                JOptionPane.showMessageDialog(null, "Paciente No Encontrado","",JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Paciente No Encontrado","",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_BotonBuscar20kActionPerformed

